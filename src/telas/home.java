    public void popularJTable(String sql) {
        try {
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3307/pousados","root","24685199");
            PreparedStatement banco = (PreparedStatement) con.prepareStatement(sql);
            banco.execute(); // cria o vetor

            ResultSet resultado = banco.executeQuery(sql);

            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.setNumRows(0);

            while (resultado.next()) {
                String disponibilidade = resultado.getBoolean("disponivel") ? "disponível" : "indisponível";
                
                model.addRow(new Object[]{
                    //retorna os dados da tabela do BD, cada campo e um coluna.
                    resultado.getString("numero_quarto"),
                    resultado.getString("preco"),
                    resultado.getString("descricao"),
                    disponibilidade
                });
            }
            banco.close();
            con.close();
        } catch (SQLException ex) {
            System.out.println("o erro foi " + ex);
        }
    }
    
